// ── LANGUAGE ENGINE ──────────────────────────────
function setLang(lang) {
  const t = translations[lang];
  if (!t) return;

  // Direction
  document.documentElement.lang = lang;
  document.documentElement.dir = (lang === 'ar' || lang === 'ku') ? 'rtl' : 'ltr';
  document.body.dir = document.documentElement.dir;

  // Translate all [data-key] elements
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
  });
  const map = { en: 0, ar: 1, ku: 2 };
  const btns = document.querySelectorAll('.lang-btn');
  if (btns[map[lang]]) btns[map[lang]].classList.add('active');

  // Save preference
  try { localStorage.setItem('ararat_lang', lang); } catch(e) {}
}

// ── SCROLL REVEAL ─────────────────────────────────
function initReveal() {
  const els = document.querySelectorAll(
    '.room-card, .amenity-item, .staff-card, .value-item, .room-detail-info, .room-detail-visual, .manager-grid, .stat, .about-badge'
  );
  els.forEach(el => el.classList.add('reveal'));

  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('visible');
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.12 });

  els.forEach(el => obs.observe(el));
}

// ── STICKY HEADER SHADOW ─────────────────────────
function initHeader() {
  const h = document.querySelector('.site-header');
  window.addEventListener('scroll', () => {
    h.style.boxShadow = window.scrollY > 10
      ? '0 2px 20px rgba(0,0,0,0.25)'
      : 'none';
  }, { passive: true });
}

// ── ANCHOR SMOOTH SCROLL ─────────────────────────
function initAnchors() {
  document.querySelectorAll('a[href*="#"]').forEach(link => {
    link.addEventListener('click', e => {
      const [page, hash] = link.getAttribute('href').split('#');
      const samePage = !page || page === window.location.pathname.split('/').pop() || page === '';
      if (samePage && hash) {
        const target = document.getElementById(hash);
        if (target) {
          e.preventDefault();
          target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
      }
    });
  });
}

// ── INIT ─────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  let savedLang = 'en';
  try { savedLang = localStorage.getItem('ararat_lang') || 'en'; } catch(e) {}
  setLang(savedLang);
  initReveal();
  initHeader();
  initAnchors();
});
